package com.basic;

public class Typeconversion 
{
public static void main(String[] args) 
	
	{
		 int i1=125;
		 long l1;
		 l1=i1;
		 System.out.println("implicit is "+l1);
		 int x1;
		 x1=(int)l1;
		 System.out.println("explicit is "+x1);
		 int a=128;
		 byte b;
		 b=(byte)a;
		 System.out.println("byte "+b);
	
   }
}